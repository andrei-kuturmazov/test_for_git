public class Person {
    private int age;
    private String name;
    private int home;

    public Person(int age, String name, int home) {
        this.age = age;
        this.name = name;
        this.home = home;
    }

    public int getAge() {
        return age;
    }

    public int getHome() {
        return home;
    }

    public String getName() {
        return name;
    }
}
